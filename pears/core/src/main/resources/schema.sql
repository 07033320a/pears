CREATE TABLE USERS(ID INTEGER, USERNAME VARCHAR_IGNORECASE(50) NOT NULL PRIMARY KEY,PASSWORD VARCHAR_IGNORECASE(500) NOT NULL,ENABLED BOOLEAN NOT NULL);
CREATE TABLE ROLES(ID INTEGER, NAME VARCHAR2(64));
CREATE TABLE RESOURCES(ID INTEGER, URL VARCHAR2(255), METHOD VARCHAR2(16));
CREATE TABLE USERS_ROLES(USER_ID INTEGER, ROLE_ID INTEGER);
CREATE TABLE USERS_ACCESSTOKEN(USER_ID INTEGER, ACCESS_TOKEN VARCHAR2(255), PERSISTENT INTEGER(1) NOT NULL);
CREATE TABLE ROLES_RESOURCES(ROLE_ID INTEGER, RESOURCE_ID INTEGER);